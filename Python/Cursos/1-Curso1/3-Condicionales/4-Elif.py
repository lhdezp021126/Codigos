num=int(input('Insert a number from one to five: '));

if num==1:
    print('The number is 1');
elif num==2:
    print('The number is 2');
elif num==3:
    print('The number is 3');
elif num==4:
    print('The number is 4');
elif num==5:
    print('The number is 5');
else:
    print('The number is out of range');